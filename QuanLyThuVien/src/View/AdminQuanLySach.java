    private void butTimkiemDGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butTimkiemDGActionPerformed
        String input = JOptionPane.showInputDialog(this, "Nhập tên đọc giả cần tìm kiếm !!!!");
        if(input != null && input.length() > 0) {
            docgiaList = docgia_Dao.findByFullname(input);
            
            tableModelDG.setRowCount(0);
        
       docgiaList.forEach((docgia) -> {
            tableModelDG.addRow(new Object[] {tableModelDG.getRowCount() + 1, docgia.getTendocgia(), 
                docgia.getSdt(), docgia.getEmail(),docgia.getDiachi()});
        });
        }else {
            showDocgia();
            
        }
        
    }//GEN-LAST:event_butTimkiemDGActionPerformed

